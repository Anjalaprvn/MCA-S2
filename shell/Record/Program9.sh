#!/bin/bash

echo "Enter a number:"
read n
sum=$(( n * (n + 1) / 2 ))

echo "Sum = $sum"


Algorithm
Step 1: Start the program
Step 2: Display the message: "Enter a number:"
Step 3: Read the number and store it in variable n
Step 4: Calculate the sum of the first n natural numbers using the formula:
        sum=𝑛(𝑛+1)/2  and store the result in variable sum
Step 5: Display the message: "Sum = $sum"
Step 6: End the program
